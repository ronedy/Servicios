package comi.carlos.servicios.Modelos;

public class ModeloRecyclerUsuarios {

     private String uid; //el uid del servicio, no lo especificamos porque esto must match with firebase data ;)
    String nombre;
    String telefono;
    String direccion;
    String urlFoto;

    public ModeloRecyclerUsuarios(){

    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getUrlFoto() {
        return urlFoto;
    }

    public void setUrlFoto(String urlFoto) {
        this.urlFoto = urlFoto;
    }




}
